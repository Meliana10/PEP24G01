sir = input("Introduceti un sir: ")
# metoda format()
print("Lungimea sirului este: {} ".format(len(sir)))

# metoda f string
print(f"Lungimea sirului este: {len(sir)}")

# metoda concatenare+
print("Lungimea sirului este: " + str(len(sir)))

# metoda cu virgula
print("Lungimea textului este:", len(sir))
