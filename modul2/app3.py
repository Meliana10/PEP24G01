# Se va cere input de la utilizator 2 numere. Se calculeaza:
# a. media numerelor
a = int(input('Introdu primul numar: '))
b = int(input('Introdu al doilea numar: '))
media = (a + b) / 2
print(media)

# b. impartirea primului nr la al doilea
imp = a // b
print("Integer division =", imp)

# c. ridicarea la putere. al doilea nr la primul
ridic = a ** b
print("Ridicare la putere =", ridic)
